-- 复习通知表
CREATE TABLE IF NOT EXISTS `mistake_notification` (
    `id` BIGINT NOT NULL AUTO_INCREMENT,
    `user_id` BIGINT NOT NULL COMMENT '接收用户ID',
    `type` VARCHAR(50) DEFAULT 'REVIEW_REMINDER' COMMENT 'REVIEW_REMINDER | MASTERY_MILESTONE | STAGE_MASTERED',
    `title` VARCHAR(200) NOT NULL COMMENT '通知标题',
    `content` VARCHAR(500) COMMENT '通知正文',
    `is_read` TINYINT DEFAULT 0 COMMENT '0=未读 1=已读',
    `created_at` DATETIME DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (`id`),
    INDEX `idx_user_read` (`user_id`, `is_read`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='错题本通知';
